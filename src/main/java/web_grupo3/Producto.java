package web_grupo3;

public class Producto {
	private int id_product;
	private String tipo_product;
	private String marca_product;
	private String descripcion_product;
	private String img_product;
	private double precio_product;
	public int getId_product() {
		return id_product;
	}
	public void setId_product(int id_product) {
		this.id_product = id_product;
	}
	public String getTipo_product() {
		return tipo_product;
	}
	public void setTipo_product(String tipo_product) {
		this.tipo_product = tipo_product;
	}
	public String getMarca_product() {
		return marca_product;
	}
	public void setMarca_product(String marca_product) {
		this.marca_product = marca_product;
	}
	public String getDescripcion_product() {
		return descripcion_product;
	}
	public void setDescripcion_product(String descripcion_product) {
		this.descripcion_product = descripcion_product;
	}
	public String getImg_product() {
		return img_product;
	}
	public void setImg_product(String img_product) {
		this.img_product = img_product;
	}
	public double getPrecio_product() {
		return precio_product;
	}
	public void setPrecio_product(double precio_product) {
		this.precio_product = precio_product;
	}
	
	
}
